def auto_support_answer(message):
    # простая логика для бота
    if "комиссия" in message.lower():
        return "Комиссия — 0.5% с каждой стороны. Если у вас больше 50 сделок — комиссия 0.25%."
    elif "верификация" in message.lower():
        return "Верификация проводится вручную, ожидайте одобрения администратора."
    else:
        return "Ваш вопрос принят. Если нужен оператор — нажмите 'Связаться с оператором'."

